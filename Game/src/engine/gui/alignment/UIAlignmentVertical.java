package engine.gui.alignment;

/**
 * Vertical alignment enum defines
 * the different position that children
 * components of a panel may take when 
 * the space in the panel is greater than
 * the maximum size of children components
 * @author louis
 *
 */
public enum UIAlignmentVertical {

	/** If Using top vertical alignment, component will be aligned to top */
	TOP,
	/** If Using Center vertical alignment, component will be aligned to center */
	CENTER,
	/** If Using Bottom vertical alignment, component will be aligned to bottom */
	BOTTOM,
}
