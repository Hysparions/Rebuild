package engine.gui.alignment;

/**
 * Horizontal alignment enum defines
 * the different position that children
 * components of a panel may take when 
 * the space in the panel is greater than
 * the maximum size of children components
 * @author louis
 *
 */
public enum UIAlignmentHorizontal {

	/** If Using Left horizontal alignment, component will be aligned to Left */
	LEFT,
	/** If Using Center horizontal alignment, component will be aligned to center */
	CENTER,
	/** If Using Right horizontal alignment, component will be aligned to Right */
	RIGHT,
}
